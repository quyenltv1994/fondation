                                            <div class="field-item even" property="content:encoded"><p>Air France &nbsp;est partenaire de la Première Edition des Palmes du Tourisme Durable. Ces palmes ont pour but de valoriser les professionnels responsables, de récompenser les meilleures initiatives de Tourisme Durable, de révéler les solutions de demain et de partager avec le plus grand nombre ces actions concrètes et duplicables. Cet événement s'inscrit dans le cadre de l'Année Internationale du Tourisme Durable pour le Développement*. En s'y associant, Air France manifeste son engagement pour faire du tourisme un catalyseur de changement positif.</p>
                                                <p>Près de 100 opérateurs touristiques professionnels ont candidaté dans les 6 catégories proposées (Hébergement, Voyage, Transport, Information, Loisirs, Territoire). Les lauréats sont :<br>
                                                    Catégorie Hébergement : Ecolodge La Belle Verte, Région Bretagne&nbsp;<br>
                                                    Catégorie Voyage : Terre des Andes, Région Occitanie<br>
                                                    Catégorie Transport : Green On, Région Île-de-France<br>
                                                    Catégorie Information : Babel Voyages, Région Île-de-France<br>
                                                    Catégorie Loisir : Echologia, Région Pays de la Loire<br>
                                                    Catégorie Territoire : Réseau des Grands Sites de France</p>
                                                <h2>Un enjeu de taille</h2>
                                                <p>Le tourisme, c'est aujourd'hui plus d'un milliard de voyageurs qui sillonnent le monde, un objectif de 100 millions de visiteurs étrangers en 2020 en France et une prévision de 2 milliards de touristes internationaux à l'échelle mondiale pour 2030. &nbsp;S'il est bien géré, le tourisme apporte de l'activité, du développement, des emplois et aide des millions de gens à sortir de la pauvreté.&nbsp;<br>
                                                    L'enjeu du Tourisme Durable est donc bien de permettre aux générations futures de pouvoir voyager dans des destinations authentiques et préservées, où le profit se marie avec la protection de l'environnement, des ressources naturelles et la protection sociale et économique. Le voyage devient alors un levier de développement et de solidarité internationale qui permet de lutter contre la pauvreté et de préserver l'environnement local, encourager la compréhension mutuelle et soutenir les efforts pour instaurer une culture de la paix.</p>
                                                <h2>Air France, acteur du Tourisme Durable</h2>
                                                <p>Air France s'inscrit pleinement dans cette perception très large du Tourisme Durable à la fois économique, environnementale et sociétale, qui rejoint ses engagements en matière de développement durable. Air France met à profit l'Année Internationale du Tourisme Durable pour le Développement pour sensibiliser ses passagers au travers d'Air France Magazine, ou encore d'un court métrage sur le tourisme responsable, toujours proposé dans l'offre de divertissement de tous ses vols Long Courrier. La compagnie soutient également de nombreux projets humanitaires, sociaux ou de préservation de la nature grâce à des partenariats pérennes. Toutes ces actions visent à soutenir un tourisme vertueux, et le développement des populations dans les pays desservis par Air France.&nbsp;</p>
                                                <p><em>&nbsp;*L'ONU a souhaité sensibiliser tout au long de l'année 2017 les décideurs et le grand public à la contribution du tourisme et mobiliser toutes les parties prenantes pour un tourisme plus durable.</em></p>
