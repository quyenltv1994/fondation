                        de 320 destinations et 118&nbsp;pays. Air France est membre fondateur de l’alliance mondial Skyteam, aux côtés de
                        Korean Air, Aeromexico et Delta. Avec la compagnie nord-américaine, Air France a par ailleurs monté une «&nbsp;joint-venture&nbsp;» dédiée
