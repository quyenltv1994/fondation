                    <h2>Profil</h2>
                    <div class="description">
                        <p>Fondée en 1933, Air France est la première compagnie aérienne française et, avec KLM, l’un des transporteurs aériens les plus importants au niveau mondial en termes de chiffre d’affaires et de voyageurs transportés.</p>

                        <p>Son activité se répartit entre le transport aérien de passagers, son cœur de métier, le fret, la maintenance et l’entretien aéronautiques, les domaines connexes du <em>catering </em>(commissariat aérien) à travers sa filiale Servair et des services à travers Air France Consulting.</p>

                        <p>En 2016 le groupe Air France-KLM a enregistré un chiffre d’affaires global de 24,8 milliards d’euros dont 79 % pour l’activité passage, 8 % pour l’activité cargo et 8% pour la maintenance.</p>
